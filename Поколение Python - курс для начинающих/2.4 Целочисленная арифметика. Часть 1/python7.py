"""Int function."""


def main():
    """My function."""
    num_1 = int(input())
    num_2 = int(input())
    sum_2 = num_1 + num_2
    diff_2 = num_1 - num_2
    prod_2 = num_1 * num_2
    print(f"{num_1} + {num_2} = {sum_2}")
    print(f"{num_1} - {num_2} = {diff_2}")
    print(f"{num_1} * {num_2} = {prod_2}")


if __name__ == "__main__":
    main()
